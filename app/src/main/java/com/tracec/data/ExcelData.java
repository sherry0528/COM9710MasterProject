package com.tracec.data;

public class ExcelData {
    private String date;
    private String pid;
    private String pname;
    private String pdob;
    private String closingtimes;
    private String staytime;
    private String warningtimes;

    public String getDate() {
        return date;
    }

    public void setDate(String date) {
        this.date = date;
    }

    public String getPid() {
        return pid;
    }

    public void setPid(String pid) {
        this.pid = pid;
    }

    public String getPname() {
        return pname;
    }

    public void setPname(String pname) {
        this.pname = pname;
    }

    public String getPdob() {
        return pdob;
    }

    public void setPdob(String pdob) {
        this.pdob = pdob;
    }

    public String getClosingtimes() {
        return closingtimes;
    }

    public void setClosingtimes(String closingtimes) {
        this.closingtimes = closingtimes;
    }

    public String getStaytime() {
        return staytime;
    }

    public void setStaytime(String staytime) {
        this.staytime = staytime;
    }

    public String getWarningtimes() {
        return warningtimes;
    }

    public void setWarningtimes(String warningtimes) {
        this.warningtimes = warningtimes;
    }
}
